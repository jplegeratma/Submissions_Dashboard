-- add in 3 month windows - with WH_MON min fields


select WH_MON, DOS_MON, TOTAL_RECS, TOTAL_RECS_3MON, TOTAL_RECS_6_MNTH_RLNG_AV, TOTAL_RECS_6_MNTH_RLNG_AV_3MON
from DOS_ACO_RLNG_6_3MON
order by 
WH_MON DESC, 
DOS_MON DESC
