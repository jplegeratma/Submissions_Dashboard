-- Calc Percent - can also do in Tableau
CREATE TABLE WH_COUNTS_5_2
AS

SELECT 
RUN_DATE, 
CDE_ENTITY_MODEL, 
ENTITY_PIDSL, 
ENTITY_NAME, 
CLAIM_MCE, 
CLAIM_ACO_MCE, 
WH_MON, 
DOS_RECS_3MON,
DOS_RECS_3MON_12_MNTH_RLNG_AV,
CASE
    WHEN DOS_RECS_3MON_12_MNTH_RLNG_AV = 0 THEN 0
    ELSE DOS_RECS_3MON / DOS_RECS_3MON_12_MNTH_RLNG_AV 
END AS DOS_RECS_3MON_PCT,
DOS_PAID_3MON,
DOS_PAID_3MON_12_MNTH_RLNG_AV,
CASE
    WHEN DOS_PAID_3MON_12_MNTH_RLNG_AV = 0 THEN 0
    ELSE DOS_PAID_3MON / DOS_PAID_3MON_12_MNTH_RLNG_AV 
END AS DOS_PAID_3MON_PCT 
FROM (

-- rolling WH MON average 12 months over 3 MON DOS
SELECT 
RUN_DATE, 
CDE_ENTITY_MODEL, 
ENTITY_PIDSL, 
ENTITY_NAME, 
CLAIM_MCE, 
CLAIM_ACO_MCE, 
WH_MON, 
DOS_RECS_3MON,

    AVG(DOS_RECS_3MON)
        OVER (
        PARTITION BY CLAIM_MCE, CLAIM_ACO_MCE 
        ORDER BY WH_MON
        ROWS BETWEEN 11 PRECEDING AND CURRENT ROW) AS DOS_RECS_3MON_12_MNTH_RLNG_AV,

DOS_PAID_3MON,
    AVG(DOS_PAID_3MON)
        OVER (
        PARTITION BY CLAIM_MCE, CLAIM_ACO_MCE
        ORDER BY WH_MON
        ROWS BETWEEN 11 PRECEDING AND CURRENT ROW) AS DOS_PAID_3MON_12_MNTH_RLNG_AV
FROM (

-- now at WH MON - push 3 month sum onto prev months
SELECT 
RUN_DATE, 
CDE_ENTITY_MODEL, 
ENTITY_PIDSL, 
ENTITY_NAME, 
CLAIM_MCE, 
CLAIM_ACO_MCE, 
WH_MON, 
--DOS_MON, 
--TOTAL_RECS,
MAX(TOTAL_RECS_3MON) AS DOS_RECS_3MON,
--TOTAL_PAID,
MAX(TOTAL_PAID_3MON) AS DOS_PAID_3MON
        
FROM (

-- Sum for WH_MON over last 4 DOS (4 because none in WH_MON = DOS_MON except maybe at end of month?)
SELECT 
RUN_DATE, 
CDE_ENTITY_MODEL, 
ENTITY_PIDSL, 
ENTITY_NAME, 
CLAIM_MCE, 
CLAIM_ACO_MCE, 
WH_MON, 
DOS_MON, 
TOTAL_RECS,

    SUM(TOTAL_RECS)
        OVER (
        PARTITION BY CLAIM_MCE, CLAIM_ACO_MCE, WH_MON
        ORDER BY DOS_MON
        ROWS BETWEEN 3 PRECEDING AND CURRENT ROW) AS TOTAL_RECS_3MON,

TOTAL_PAID,

    SUM(TOTAL_PAID)
        OVER (
        PARTITION BY CLAIM_MCE, CLAIM_ACO_MCE, WH_MON
        ORDER BY DOS_MON
        ROWS BETWEEN 3 PRECEDING AND CURRENT ROW) AS TOTAL_PAID_3MON

        
FROM (
-- counts and sum for DOS
SELECT 
RUN_DATE, 
CDE_ENTITY_MODEL, 
ENTITY_PIDSL, 
ENTITY_NAME, 
CLAIM_MCE, 
CLAIM_ACO_MCE, 
WH_MON, 
DOS_MON, 
COUNT(ENC_CLAIM_NO||ENC_CLAIM_SUFFIX) AS TOTAL_RECS,
SUM(AMT_PAID) AS TOTAL_PAID,
FROM (
-- core
SELECT
--TO_DATE('20250225','YYYYMMDD') AS RUN_DATE,
CURRENT_DATE() AS RUN_DATE,
                 cw.CDE_ENTITY_MODEL, 
                 cw.ENTITY_PIDSL,
                 cw.ENTITY_NAME,
                 E.CDE_ENC_MCO AS CLAIM_MCE,
                 CASE WHEN E.CDE_ENC_ACO in('#','+','-') 
                     THEN E.CDE_ENC_MCO ELSE E.CDE_ENC_ACO END AS CLAIM_ACO_MCE,
                 ENC_CLAIM_NO,
                 ENC_CLAIM_SUFFIX,
                 TO_CHAR(DOS_FROM_DT,'YYMM') AS DOS_MON,
                 TO_CHAR(WH_FROM_DT,'YYMM') AS WH_MON,
                 AMT_PAID
                 FROM mhdwprod.nw.nw_encounter_hist e
         left join mhteam.dwdq.INF_B_MCE_PIDSL_CROSSWALK cw on cw.mco = e.cde_enc_mco and cw.aco = e.cde_enc_aco            
         WHERE e.dos_from_dt >= '01-JAN-2022'
         AND e.IND_OFFSET = 'N'
--and E.CDE_ENC_ACO = 'BMC-BACO'
)
GROUP BY RUN_DATE, 
         CDE_ENTITY_MODEL, 
         ENTITY_PIDSL, 
         ENTITY_NAME, 
         CLAIM_MCE, 
         CLAIM_ACO_MCE, 
         WH_MON, 
         DOS_MON

ORDER BY RUN_DATE, CDE_ENTITY_MODEL, ENTITY_PIDSL, ENTITY_NAME, CLAIM_MCE, CLAIM_ACO_MCE, WH_MON, DOS_MON

)
GROUP BY RUN_DATE, 
         CDE_ENTITY_MODEL, 
         ENTITY_PIDSL, 
         ENTITY_NAME, 
         CLAIM_MCE, 
         CLAIM_ACO_MCE, 
         WH_MON, 
         DOS_MON,
         TOTAL_RECS,
         TOTAL_PAID
HAVING DOS_MON >= TO_CHAR(DATEADD(month, -3, TO_DATE(WH_MON,'YYMM')),'YYMM')

--ORDER BY RUN_DATE, CDE_ENTITY_MODEL, ENTITY_PIDSL, ENTITY_NAME, CLAIM_MCE, CLAIM_ACO_MCE, WH_MON DESC, DOS_MON DESC

)

GROUP BY RUN_DATE, 
         CDE_ENTITY_MODEL, 
         ENTITY_PIDSL, 
         ENTITY_NAME, 
         CLAIM_MCE, 
         CLAIM_ACO_MCE, 
         WH_MON 

--ORDER BY RUN_DATE, CDE_ENTITY_MODEL, ENTITY_PIDSL, ENTITY_NAME, CLAIM_MCE, CLAIM_ACO_MCE, WH_MON DESC


-- Sum for WH_MON over last 4 DOS (4 because none in WH_MON = DOS_MON except maybe at end of month?)
)
GROUP BY RUN_DATE, 
         CDE_ENTITY_MODEL, 
         ENTITY_PIDSL, 
         ENTITY_NAME, 
         CLAIM_MCE, 
         CLAIM_ACO_MCE, 
         WH_MON, 
         DOS_RECS_3MON,
         DOS_PAID_3MON

--ORDER BY RUN_DATE, CDE_ENTITY_MODEL, ENTITY_PIDSL, ENTITY_NAME, CLAIM_MCE, CLAIM_ACO_MCE, WH_MON DESC

-- percent
)
ORDER BY RUN_DATE, CDE_ENTITY_MODEL, ENTITY_PIDSL, ENTITY_NAME, CLAIM_MCE, CLAIM_ACO_MCE, WH_MON DESC
;




